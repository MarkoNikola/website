// ═══════════════════════════════════════════════════════════════
//  ENGINE – Applies language to all elements with data-i18n
// ═══════════════════════════════════════════════════════════════

function applyLang(lang) {
  const t = LANG[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      const attr = el.getAttribute('data-i18n-attr');
      if (attr) {
        el.setAttribute(attr, t[key]);
      } else {
        el.textContent = t[key];
      }
    }
  });
  // Sync flag button active state
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('lang-active', b.dataset.lang === lang);
  });
  // Update <html lang> attribute
  document.documentElement.lang = lang;
}

function setLang(lang) {
  localStorage.setItem('it_lang', lang);
  applyLang(lang);
  // Re-render product cards if on showroom/product page
  if (typeof filterCards === 'function') filterCards();
}

function getLang() {
  return localStorage.getItem('it_lang') || 'hr';
}

// Auto-apply on page load
document.addEventListener('DOMContentLoaded', () => applyLang(getLang()));
